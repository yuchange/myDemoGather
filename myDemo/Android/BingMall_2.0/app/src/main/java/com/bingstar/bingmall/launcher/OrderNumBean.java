public class OrderNumBean {
    private String unPayNum;
    private String unTransNum;
    private String unCheckNum;

    public String getUnPayNum() {
        if (unPayNum==null||unPayNum.equals("")){
            return "0";
        }
        return unPayNum;
    }

    public String getUnTransNum() {
        if (unTransNum==null||unTransNum.equals("")){
            return "0";
        }
        return unTransNum;
    }

    public String getUnCheckNum() {
        if (unCheckNum==null||unCheckNum.equals("")){
            return "0";
        }
        return unCheckNum;
    }

    public void setUnPayNum(String unPayNum) {
        this.unPayNum = unPayNum;
    }

    public void setUnTransNum(String unTransNum) {
        this.unTransNum = unTransNum;
    }

    public void setUnCheckNum(String unCheckNum) {
        this.unCheckNum = unCheckNum;
    }
}
