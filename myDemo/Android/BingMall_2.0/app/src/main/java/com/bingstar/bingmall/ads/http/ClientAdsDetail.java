public class ClientAdsDetail {
    private String home_id;//广告主键ID
    private String homepage_name;//	广告名称
    private String description;//	广告描述
    private String num_id;//广告编号
    private String numSort;//当前编号下有多个图片时排序
    private String imgUrl;//图片地址
    private String videoUrl;//视频地址
    private String parameter_url;//	动态图的链接
    private String type;//	广告类型（0：静态图片 1：动态图片2：视频）
    private String pageStyle;//页面风格（5：分5块区域，6：分6块区域）

    public void setHome_id(String home_id) {
        this.home_id = home_id;
    }

    public void setHomepage_name(String homepage_name) {
        this.homepage_name = homepage_name;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public void setNum_id(String num_id) {
        this.num_id = num_id;
    }

    public void setNumSort(String numSort) {
        this.numSort = numSort;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public void setVideoUrl(String videoUrl) {
        this.videoUrl = videoUrl;
    }

    public void setParameter_url(String parameter_url) {
        this.parameter_url = parameter_url;
    }

    public void setType(String type) {
        this.type = type;
    }

    public void setPageStyle(String pageStyle) {
        this.pageStyle = pageStyle;
    }

    public String getHome_id() {
        return home_id;
    }

    public String getHomepage_name() {
        return homepage_name;
    }

    public String getDescription() {
        return description;
    }

    public String getNum_id() {
        return num_id;
    }

    public String getNumSort() {
        return numSort;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public String getVideoUrl() {
        return videoUrl;
    }

    public String getParameter_url() {
        return parameter_url;
    }

    public String getType() {
        return type;
    }

    public String getPageStyle() {
        return pageStyle;
    }
}
