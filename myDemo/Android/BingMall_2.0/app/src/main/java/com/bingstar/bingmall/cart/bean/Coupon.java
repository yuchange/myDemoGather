package com.bingstar.bingmall.cart.bean;

import java.util.ArrayList;

/**
 * Created by qianhechen on 17/3/20.
 */

public class Coupon {
    public ArrayList<CouponInfo> couponInfoList;

    public ArrayList<CouponInfo> getCouponInfoList() {
        return couponInfoList;
    }

    public void setCouponInfoList(ArrayList<CouponInfo> couponInfoList) {
        this.couponInfoList = couponInfoList;
    }

    public class CouponInfo {
        private String couponId;
        private String couponMoney;
        private String couponDetail;
        private String couponValidity;
        private String couponRange;
        private String couponNum;
        private String couponName;
        private String couponType;  //（1.全场通用 2.类目使用 3.部分商品）
        private boolean isSelected;
        private String remindText; //优惠卷提示信息
        private String remindFlag;//是否高亮

        @Override
        public String toString() {
            return "CouponInfo{" +
                    "couponId='" + couponId + '\'' +
                    ", couponMoney='" + couponMoney + '\'' +
                    ", couponDetail='" + couponDetail + '\'' +
                    ", couponValidity='" + couponValidity + '\'' +
                    ", couponRange='" + couponRange + '\'' +
                    ", couponNum='" + couponNum + '\'' +
                    ", couponName='" + couponName + '\'' +
                    ", couponType='" + couponType + '\'' +
                    ", isSelected=" + isSelected +
                    '}';
        }


        public String getRemindText() {
            return remindText;
        }

        public void setRemindText(String remindText) {
            this.remindText = remindText;
        }

        public String getRemindFlag() {
            return remindFlag;
        }

        public void setRemindFlag(String remindFlag) {
            this.remindFlag = remindFlag;
        }
        public String getCouponType() {
            return couponType;
        }

        public void setCouponType(String couponType) {
            this.couponType = couponType;
        }


        public String getCouponId() {
            return couponId;
        }

        public void setCouponId(String couponId) {
            this.couponId = couponId;
        }

        public String getCouponMoney() {
            return couponMoney;
        }

        public void setCouponMoney(String couponMoney) {
            this.couponMoney = couponMoney;
        }

        public String getCouponDetail() {
            return couponDetail;
        }

        public void setCouponDetail(String couponDetail) {
            this.couponDetail = couponDetail;
        }

        public String getCouponValidity() {
            return couponValidity;
        }

        public void setCouponValidity(String couponValidity) {
            this.couponValidity = couponValidity;
        }

        public String getCouponRange() {
            return couponRange;
        }

        public void setCouponRange(String couponRange) {
            this.couponRange = couponRange;
        }

        public String getCouponNum() {
            return couponNum;
        }

        public void setCouponNum(String couponNum) {
            this.couponNum = couponNum;
        }

        public boolean isSelected() {
            return isSelected;
        }

        public void setSelected(boolean selected) {
            isSelected = selected;
        }

        public String getCouponName() {
            return couponName;
        }

        public void setCouponName(String couponName) {
            this.couponName = couponName;
        }
    }

}
