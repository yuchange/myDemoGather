package com.zyf.bings.bingos.ui;

import android.content.Context;
import android.support.v4.widget.NestedScrollView;
import android.util.AttributeSet;
import android.util.Log;

/**
 * Created by zhangyifei on 17/9/10.
 */

public class ResizeScrollVIew extends NestedScrollView {
    public ResizeScrollVIew(Context context) {
        super(context);
    }

    public ResizeScrollVIew(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    public ResizeScrollVIew(Context context, AttributeSet attrs, int defStyleAttr) {
        super(context, attrs, defStyleAttr);
    }

    @Override
    protected void onSizeChanged(int w, int h, int oldw, int oldh) {
        super.onSizeChanged(w, h, oldw, oldh);
        Log.e("ResizeRelativeLayout", "onSizeChanged: " + "");
    }

    @Override
    protected void onMeasure(int widthMeasureSpec, int heightMeasureSpec) {
        super.onMeasure(widthMeasureSpec, heightMeasureSpec);
        Log.e("ResizeRelativeLayout", "onMeasure: " + "");
    }

    @Override
    protected void onLayout(boolean changed, int l, int t, int r, int b) {
        super.onLayout(changed, l, t, r, b);
        Log.e("ResizeRelativeLayout", "onLayout: " + "");
    }
}
